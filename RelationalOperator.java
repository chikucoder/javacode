//=  Assignment operator
//== Relational operator

public class RelationalOperator {
    public static void main(String[] args) {
        int p=10; int q=20;

        System.out.println(p==q);  //false
        System.out.println(p<q);  //true
        System.out.println(p>q);  //false
        System.out.println(p<=q); //true
        System.out.println(p>=q); // false
    }
    
    
}
